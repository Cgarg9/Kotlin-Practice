fun main() {

    var myName = "Chirag" // can reassign variables starting with var
    println("myName is " + myName)
    println("myName is $myName") // no need for curly braces if a single variable is used.

    val pi = 3.14
    println("pi is $pi")
    // pi = 5.1 this is wrong
    var a = 34
    var b = 45
    println("a is $a and b is $b and sum is ${a + b}")

}